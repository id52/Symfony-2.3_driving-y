<?php

namespace My\AppBundle\Model;

/**
 * TestEmail
 */
abstract class TestEmail
{
    /**
     * @var integer
     */
    protected $id;

    /**
     * @var string
     */
    protected $email;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set email
     *
     * @param string $email
     * @return TestEmail
     */
    public function setEmail($email)
    {
        $this->email = $email;

        return $this;
    }

    /**
     * Get email
     *
     * @return string 
     */
    public function getEmail()
    {
        return $this->email;
    }
}
